#include "World.h"

#include <SFML/Graphics/RenderWindow.hpp>

World::World( sf::RenderWindow& window )
: mWindow( window )
, mWorldView( window.getDefaultView() )
, mTextures()
, mSceneGraph()
, mSceneLayers()
, mWorldBounds( 0.f, 0.f, 960, 640 )
, mSpawnPosition( 960 / 2.f,  640 / 2.f )
, mScrollSpeed( -50.f )
{
	loadTextures();
	buildScene();

	// Prepare the view
	mWorldView.setCenter( mSpawnPosition );
}

void
World::update( sf::Time dt )
{
	// Scroll the world
	//mWorldView.move( 0.f, mScrollSpeed * dt.asSeconds());
	mSceneGraph.update(dt);
}

void
World::draw()
{
	//mWindow.setView( mWorldView );
	mWindow.draw(mMap);
	mWindow.draw( mSceneGraph );
}

void
World::loadTextures()
{
	mTextures.load( Textures::Hero1,  	"assets/textures/Hero.png");
}

